package com.operator;

public class RelationalOperatorPrg {

	public static void main(String[] args) {
		
		///Relational Operator... symbols:  <, <=, >, >=, ==
		
		int var1 = 100;    ////Assignment Operator...  symbol ( = )
		int var2 = 100;
				
		if(var1 < var2) {
			System.out.println("Var1 is Smaller than Var2");
		}
				
		if(var1 > var2) {
	    	System.out.println("Var1 is Greater than Var2");
		}
				
		if(var1 == var2) {
			System.out.println("Var1 is equal to Var2");
		}


	}

}
